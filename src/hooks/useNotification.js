import Constants from "expo-constants";
import * as Notifications from "expo-notifications";
import React, { useState, useEffect, useRef } from "react";
import { Text, View, Button, Platform } from "react-native";
import expoPushTokenApi from "../api/expoPushTokens";
import appNavigation from "../navigation/rootNavigation2";

Notifications.setNotificationHandler({
  handleNotification: async () => ({
    shouldShowAlert: true,
    shouldPlaySound: false,
    shouldSetBadge: true,
  }),
});

const useNotification = () => {
  const [expoPushToken, setExpoPushToken] = useState("");
  const [notification, setNotification] = useState(false);
  const [notifications, setNotifications] = useState([]);
  const notificationListener = useRef();
  const responseListener = useRef();

  useEffect(() => {
    registerForPushNotificationsAsync().then((token) =>
      setExpoPushToken(token)
    );

    notificationListener.current = Notifications.addNotificationReceivedListener(
      (notification) => {
        setNotification(notification);
      }
    );

    responseListener.current = Notifications.addNotificationResponseReceivedListener(
      (response) => {
        if (response.notification.request.content.data) {
          if (
            response.notification.request.content.data?.type === "order:new"
          ) {
            appNavigation.navigate("Root", {
              screen: "OrderTab",
            });
          }
          if (
            response.notification.request.content.data?.type ===
            "transaction:update"
          ) {
            appNavigation.navigate("Root", {
              screen: "WalletTab",
            });
          }
          if (
            response.notification.request.content.data?.type === "support:chat"
          ) {
            appNavigation.navigate("SupportPage");
          }
        }
      }
    );

    return () => {
      Notifications.removeNotificationSubscription(
        notificationListener.current
      );
      Notifications.removeNotificationSubscription(responseListener.current);
    };
  }, []);
};

export default useNotification;

